    1,099,511,627,776 Gigabytes, or 1,125,899,910,000,000 Megabytes.</dd>
  <dt>Yottabyte (YB)</dt>
  <dd>A <b>Yottabyte</b> is 1,208,925,819,614,629,174,706,176 (280) bytes,
     1,024 Zettabytes, 1,048,576 Exabytes, 1,073,741,824 Petabytes,
     1,099,511,627,776 Terabytes, 1,125,899,910,000,000 Gigabytes, or
     1,152,921,500,000,000,000 Megabytes.</dd>
